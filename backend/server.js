// Graceful shutdown function
const gracefulShutdown = () => {
  console.log('\nрџ›‘ Received shutdown signal, closing server...');
  
  if (server) {
    server.close(() => {
      console.log('вњ… Server closed successfully');
      process.exit(0);
    });
    
    // Force close after 10 seconds if server doesn't close
    setTimeout(() => {
      console.error('вљ пёЏ Could not close connections in time, forcefully shutting down');
      process.exit(1);
    }, 10000);
  } else {
    process.exit(0);
  }
};

